public class TeamPojo {

	private int teamId;
	private String teamDescription;
	/**
	 * @return the id
	 */
	public int getTeamId() {
		return teamId;
	}
	/**
	 * @param argTeamId the id to set
	 */
	public void setTeamId(int argTeamId) {
		this.teamId = argTeamId;
	}
	/**
	 * @return the description
	 */
	public String getTeamDescription() {
		return teamDescription;
	}
	/**
	 * @param argTeamDescription the description to set
	 */
	public void setTeamDescription(String argTeamDescription) {
		this.teamDescription = argTeamDescription;
	}
	
	
}
